package application;

public class Cours {
	int idcours;
	String titre;
	String path;
	public Cours() {
	}
	public int getIdcours() {
		return idcours;
	}
	public void setIdcours(int idcours) {
		this.idcours = idcours;
	}
	public String getTitre() {
		return titre;
	}
	public void setTitre(String titre) {
		this.titre = titre;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	
}
